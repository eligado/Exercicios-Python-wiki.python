# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.


print('Calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.')
l = int(input('Digite o tamanho de um lado: '))

area = l * l
area2 = area * 2

print(f'A área do quadrado é de {area} e seu dobro é de {area2}')

